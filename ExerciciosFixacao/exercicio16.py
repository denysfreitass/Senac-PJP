#Leia dois int (a e b) e imprima: 
#a + b = X | a - b = Y | a * b = Z

print(f"Calcular a + b = X | a - b = Y | a * b = Z")
a = int(input("Digite o valor de A: "))
b = int(input("Digite o valor de B: "))

x = a + b
y = a - b 
z = a * b 
print(f"O valor de {a} + {b} = {x} | {a} - {b} = {y} | {a} * {b} = {z}")
