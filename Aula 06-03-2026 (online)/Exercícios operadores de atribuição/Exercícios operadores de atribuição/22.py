# Leia n (int). Faça n += 3, n *= 2, n -= 4, n //= 3. Mostre cada etapa usando a função print().

num = int(input("Digite um numero: "))
num += 3
print(f"O numero escolhido somado a 3, fica: {num}")
num *= 2 
print(f"O novo numero multiplicado por 2 fica: {num}")
num -= 4
print(f"O novo numero subtraindo 4 fica: {num}")
num //= 3 
print(f"O novo numero dividindo por igual para 3: {num}")
